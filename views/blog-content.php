        <h1 class="hidden">Formula 1’s Biggest Rivalries of All Time</h1>
        <p>
            Formula One or F1 is the highest class of single seater racing series and most widely travelled motorsport around the world. Due to its thrilling nature, auto manufacturer involvement and brands sponsorship, it is a top 10 sport
            in terms of viewership and revenue.
        </p>
        <p>
            The key reason of this fast growth of the sport is driver rivalry. Driver’s no matter teammates fight the fierce battles for the championship that makes them friends or foe forever. Here, I am sharing my list of some top driver
            rivalries in the history of Formula 1.
        </p>
        <h2>Niki Lauda (Ferrari) and James Hunt (McLaren)</h2>
        <img src="../images/laudavshunt.jpg" alt="Nikki Lauda and James Hunt rivalry" class="rivalryimages" />
        <span class="caption">Image Source: BBC</span>
        <p>
            Lauda vs Hunt rivalry is the first of its kind driver rivalry since the start of FIA Formula 1. It is also the most celebrated and prominent rivalry showcases through many documentaries, and a
            <a href="https://www.imdb.com/title/tt1979320/" target="_blank">Hollywood feature film Rush</a>. Their rivalry was not only professional but also personal.
        </p>
        <h2>Ayrton Senna and Alain Prost (McLaren)</h2>
        <img src="../images/sennavsprost.jpg" alt="Ayrton Senna and Alain Prost rivalry" class="rivalryimages" />
        <span class="caption">Image Source: Autoweek</span>
        <p>
            Ayrton Senna's feud with Alain Prost remains not only the greatest rivalry in the history of Formula One, but one of the most fierce battles in sport. Their rivalry would continue until Prost hung up his helmet at the end of
            1993, but not as team-mates as the Frenchman fled to Ferrari for '90, where he was, of course, warmly welcomed by Nigel Mansell.
        </p>
        <h2>Michael Schumacher (Ferrari) and Mika Hakkinen (McLaren)</h2>
        <img src="../images/shumachervshakkinen.jpg" alt="Michael Schumacher and Mika Hakkinen rivalry" class="rivalryimages" />
        <span class="caption">Image Source: HubPages</span>
        <p>
            Schumacher, over the years, has had many rivalries but, the one with Hakkinen is one of the most prominent ones. Although the rivalry was short-lived, it was fierce nonetheless. Hakkinen won 2 Championships consecutively in 1998
            and 1999. Schumacher had won two Championships for Benetton and won 5 more for Ferrari.
        </p>
        <h2>Sebastian Vettel and Mark Webber (Red Bull)</h2>
        <img src="../images/webbervsvettel.jpg" alt="Mark Webber and Sebastial Vettel rivalry" class="rivalryimages" />
        <span class="caption">Image Source: F1i</span>
        <p>
            The rivalry between Sebastian Vettel and Mark Webber began over a year before they became team-mates at Red Bull Racing, when they collided behind the safety car at the wet
            <a href="https://www.youtube.com/watch?v=HO1f7ah5zcE" target="_blank">2007 Japanese Grand Prix</a>, an afternoon when both drivers stood a strong chance of claiming a maiden win.
        </p>
        <p>
            There are some other rivalries too but I have picked the top most only. Share your views on these rivalries or request me to add another rivalry that you think should be included in the list by commenting below.
        </p>
